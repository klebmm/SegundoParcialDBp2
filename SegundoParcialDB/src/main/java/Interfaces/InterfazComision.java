    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed

        ClsEmpleados insert = new ClsEmpleados();

        insert.setNombre(jTextNombre.getText());
        insert.setEnero(Integer.parseInt(jTextEnero.getText()));
        insert.setFebrero(Integer.parseInt(jTextFebrero.getText()));
        insert.setMarzo(Integer.parseInt(jTextMarzo.getText()));

        empleadosJDBC.insert(insert);
    }//GEN-LAST:event_jButtonInsertActionPerformed

